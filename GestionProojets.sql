-- 2. Analyse des Délais de Projets
-- Objectif : Calculer l'écart de délai (en jours) pour les projets terminés et le délai moyen global.

SELECT
    ProjetID,
    NomProjet,
    DateFinPrevue,
    DateFinReelle,
    -- Calcul de l'écart de délai en jours (positif = retard, négatif = avance)
    -- NOTE : La fonction DATEDIFF varie selon le SGBD (ex: DATEDIFF(day, DateFinPrevue, DateFinReelle) pour SQL Server)
    (DateFinReelle - DateFinPrevue) AS EcartDelaiJours
FROM
    Projets
WHERE
    StatutProjet = 'Terminé'
ORDER BY
    EcartDelaiJours DESC;

-- Requête 2b : Délai moyen des projets terminés
SELECT
    AVG(DateFinReelle - DateFinPrevue) AS DelaiMoyenJours
FROM
    Projets
WHERE
    StatutProjet = 'Terminé';

-- 3. Rentabilité par Client et Secteur
-- Objectif : Calculer le budget réel total et le CA cumulé par secteur, et identifier les 5 clients les plus rentables (où BudgetReel > BudgetEstime). SQL

-- Requête 3a : Budget réel total et CA cumulé par Secteur d'Activité
SELECT
    C.SecteurActivite,
    SUM(P.BudgetReel) AS BudgetReelTotal,
    SUM(C.CA_Cumule) AS CACumuleTotal
FROM
    Clients C
LEFT JOIN
    Projets P ON C.ClientID = P.ClientID
GROUP BY
    C.SecteurActivite
ORDER BY
    BudgetReelTotal DESC;

-- Requête 3b : Les 5 clients les plus rentables (Budget Réel > Budget Estimé)
SELECT
    C.NomClient,
    SUM(P.BudgetReel) AS BudgetReelTotal,
    SUM(P.BudgetEstime) AS BudgetEstimeTotal,
    SUM(P.BudgetReel) - SUM(P.BudgetEstime) AS MargePositive
FROM
    Projets P
JOIN
    Clients C ON P.ClientID = C.ClientID
WHERE
    P.StatutProjet = 'Terminé' -- On ne compare que les projets terminés
GROUP BY
    C.NomClient
HAVING
    SUM(P.BudgetReel) > SUM(P.BudgetEstime)
ORDER BY
    MargePositive DESC
LIMIT 5;

-- 4. Répartition de la Charge de Travail
-- Objectif : Calculer la charge de travail totale (Heures Estimées) par équipe et spécialité, et identifier les développeurs surchargés en tâches "À faire".

-- Requête 4a : Charge de travail totale par Équipe et Spécialité
SELECT
    D.Equipe,
    D.Specialite,
    SUM(T.HeuresEstimees) AS ChargeTravailTotale
FROM
    Taches T
JOIN
    Developpeurs D ON T.DevID = D.DevID
GROUP BY
    D.Equipe,
    D.Specialite
ORDER BY
    ChargeTravailTotale DESC;

-- Requête 4b : Développeurs avec une charge de travail "À faire" supérieure à la moyenne
WITH ChargeAFaire AS (
    SELECT
        DevID,
        SUM(HeuresEstimees) AS HeuresAFaire
    FROM
        Taches
    WHERE
        StatutTache = 'À faire'
    GROUP BY
        DevID
)
SELECT
    D.NomDev,
    C.HeuresAFaire
FROM
    ChargeAFaire C
JOIN
    Developpeurs D ON C.DevID = D.DevID
WHERE
    C.HeuresAFaire > (SELECT AVG(HeuresAFaire) FROM ChargeAFaire)
ORDER BY
    C.HeuresAFaire DESC;

-- 5. Suivi des Tâches Critiques
-- Objectif : Calculer le pourcentage de tâches à priorité 'Haute' qui sont en retard.

-- NOTE : Remplacer 'CURRENT_DATE' par la fonction de date actuelle de votre SGBD (ex: GETDATE() pour SQL Server, NOW() pour PostgreSQL).
SELECT
    CAST(SUM(CASE WHEN T.StatutTache IN ('À faire', 'En cours') AND T.DateEcheance < CURRENT_DATE THEN 1 ELSE 0 END) AS REAL) * 100
    /
    COUNT(T.TacheID) AS PourcentageTachesHautePrioriteEnRetard
FROM
    Taches T
WHERE
    T.Priorite = 'Haute';


