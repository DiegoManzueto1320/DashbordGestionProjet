-- 1. Performance des Développeurs (Efficacité et Coût)
-- Objectif : Calculer le taux d'efficacité moyen (Heures Réelles / Heures Estimées) 
-- par développeur et par niveau, et le coût total des heures réelles facturées sur les projets terminés.
SELECT
    D.NomDev,
    D.Niveau,
    CAST(SUM(T.HeuresReelles) AS REAL) / NULLIF(SUM(T.HeuresEstimees), 0) AS TauxEfficaciteMoyen,
    SUM(T.HeuresReelles * D.TauxHoraire) AS CoutTotalHeuresReelles
FROM
    Taches T
JOIN
    Developpeurs D ON T.DevID = D.DevID
JOIN
    Projets P ON T.ProjetID = P.ProjetID
