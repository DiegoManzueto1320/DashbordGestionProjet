-- Utilisation de la base de données
USE BDGESPROJET; 

-- SCRIPT DE CRÉATION DES TABLES POUR LA GESTION DE PROJETS LOGICIELS

-- 1. TABLE CLIENTS
-- Table de dimension pour les informations clients
CREATE TABLE Clients (
    ClientID INT PRIMARY KEY,
    NomClient VARCHAR(255) NOT NULL,
    SecteurActivite VARCHAR(100),
    Ville VARCHAR(100),
    Pays VARCHAR(100),
    DatePremierContrat DATE,
    StatutClient VARCHAR(50),
    CA_Cumule DECIMAL(15, 2) -- Chiffre d'affaires cumulé
);

-- 2. TABLE DÉVELOPPEURS
-- Table de dimension pour les ressources humaines
CREATE TABLE Developpeurs (
    DevID INT PRIMARY KEY,
    NomDev VARCHAR(100) NOT NULL,
    PrenomDev VARCHAR(100) NOT NULL,
    Specialite VARCHAR(50), -- Ex: Frontend, Backend, Fullstack
    Niveau VARCHAR(50),     -- Ex: Junior, Confirmé, Senior
    DateEmbauche DATE,
    TauxHoraire DECIMAL(5, 2), -- Taux horaire de facturation ou coût
    Equipe VARCHAR(50),
    Statut VARCHAR(50)      -- Ex: Actif, Congé
);

-- 3. TABLE PROJETS
-- Table de faits/dimension pour les projets
CREATE TABLE Projets (
    ProjetID INT PRIMARY KEY,
    NomProjet VARCHAR(255) NOT NULL,
    ClientID INT NOT NULL,
    ChefProjetID INT NOT NULL,
    StatutProjet VARCHAR(50), -- Ex: En cours, Terminé, Annulé
    DateDebut DATE,
    DateFinPrevue DATE,
    DateFinReelle DATE,
    BudgetEstime DECIMAL(15, 2),
    BudgetReel DECIMAL(15, 2),
    TechnologiePrincipale VARCHAR(100),
    -- Clés Étrangères
    FOREIGN KEY (ClientID) REFERENCES Clients(ClientID),
    FOREIGN KEY (ChefProjetID) REFERENCES Developpeurs(DevID)
);

-- 4. TABLE TÂCHES
-- Table de faits pour le suivi des tâches
CREATE TABLE Taches (
    TacheID INT PRIMARY KEY,
    ProjetID INT NOT NULL,
    DevID INT NOT NULL,
    DescriptionTache TEXT,
    StatutTache VARCHAR(50), -- Ex: À faire, En cours, Terminée
    Priorite VARCHAR(50),    -- Ex: Haute, Moyenne, Basse
    DateCreation DATE,
    DateEcheance DATE,
    HeuresEstimees DECIMAL(6, 2),
    HeuresReelles DECIMAL(6, 2),

    -- Clés Étrangères
    FOREIGN KEY (ProjetID) REFERENCES Projets(ProjetID),
    FOREIGN KEY (DevID) REFERENCES Developpeurs(DevID)
);
